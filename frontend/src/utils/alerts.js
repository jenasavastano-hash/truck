/**
 * Simple alert utilities for the application
 */

export function alertSuccess(message) {
  alert(`✅ ${message}`);
}

export function alertError(message) {
  alert(`❌ ${message}`);
}

export function alertWarning(message) {
  alert(`⚠️ ${message}`);
}

export function alertInfo(message) {
  alert(`ℹ️ ${message}`);
}
